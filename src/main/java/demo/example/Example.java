public class Example {
    /**
     * 计算两个数的加和
     *
     * @param a1 第一个数
     * @param a2 第二个数
     * @return a1+a2
     */
    public int add(int a1, int a2) {
        return a1 + a2;
    }

    public int sub(int a1, int a2) {
        return a1 - a2;
    }
}
